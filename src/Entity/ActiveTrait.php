namespace App\Entity;

use Doctrine\ORM\Mapping as ORM;

trait ActiveTrait
{
    #[ORM\Column('active', 'boolean')]
    private bool $active = true;

    public function isActive(): bool
    {
        return $this->active;
    }

    public function activate(): void
    {
        $this->active = true;
    }

    public function deactivate(): void
    {
        $this->active = false;
    }
}
